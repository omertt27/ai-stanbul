def add_training_data():
    """Add all new training samples to the comprehensive dataset"""
    
    # Load existing data
    data_file = Path("comprehensive_training_data.json")
    with open(data_file, "r", encoding="utf-8") as f:
        existing_data = json.load(f)
    
    print(f"ğŸ“Š Current dataset: {len(existing_data)} samples")
    
    # Count existing intents
    intent_counts = Counter([item[1] for item in existing_data])
    print(f"   - local_tips: {intent_counts.get('local_tips', 0)}")
    print(f"   - hidden_gems: {intent_counts.get('hidden_gems', 0)}")
    print(f"   - events: {intent_counts.get('events', 0)}")
    print(f"   - neighborhoods: {intent_counts.get('neighborhoods', 0)}")
    
    # Collect new samples
    new_samples = []
    new_samples.extend(get_new_local_tips_samples())
    new_samples.extend(get_new_hidden_gems_samples())
    new_samples.extend(get_new_events_samples())
    new_samples.extend(get_new_neighborhoods_samples())
    
    print(f"\nğŸ“ Adding {len(new_samples)} new samples:")
    new_intent_counts = Counter([item[1] for item in new_samples])
    for intent, count in sorted(new_intent_counts.items()):
        print(f"   + {intent}: {count} samples")
    
    # Combine and save
    combined_data = existing_data + new_samples
    
    # Create backup
    backup_file = data_file.with_suffix('.backup.json')
    with open(backup_file, "w", encoding="utf-8") as f:
        json.dump(existing_data, f, ensure_ascii=False, indent=2)
    print(f"\nğŸ’¾ Backup created: {backup_file}")
    
    # Save updated data
    with open(data_file, "w", encoding="utf-8") as f:
        json.dump(combined_data, f, ensure_ascii=False, indent=2)
    
    print(f"\nâœ… Updated dataset: {len(combined_data)} samples")
    
    # Show final distribution
    final_counts = Counter([item[1] for item in combined_data])
    print(f"\nğŸ“Š Final intent distribution:")
    for intent, count in sorted(final_counts.items(), key=lambda x: x[1], reverse=True):
        print(f"   {intent:30s} {count:4d} samples")
    
    print(f"\nğŸ‰ Training data enhancement complete!")
    print(f"   Total samples: {len(existing_data)} â†’ {len(combined_data)}")
